// for the /News/Archive.html page
function toggleArchive() {
    const arcWeb = document.getElementById("archiveWeb");
    const arcMobile = document.getElementById("archiveMobile");

    arcMobile.appendChild(document.createElement("br"));

    let temp;

    for (let i = 8; i < data.articles.length-2; i++) {
        temp = makeLink(`${data.articles[i].title} [${data.articles[i].author} ${data.articles[i].year}]`, data.articles[i].url, "Click here to see this article");
        arcWeb.appendChild(temp);
        arcMobile.appendChild(temp.cloneNode(true));
        arcWeb.appendChild(document.createElement("br"));
        arcMobile.appendChild(document.createElement("br"));
    }
}

// for the /News/index.html page
function toggleNews() {
    createNewsTd(data.articles.length-2);
    for (let i = 0; i < 8; i++) {
        createNewsTd(i);
    }
    createNewsTd(data.articles.length-1);
}

// randomize the featured news article on the homepage
function randomNews() {
    let featuredArticle;
    let recentNewsletter;

    // make sure only the most recent newsletter can be featured
    for (i=0; i<data.articles.length; i++) {
        if (data.articles[i].classification == "NEWSLETTER") {
            recentNewsletter = i;
            break;
        }
    }

    let ran;
    while (true) {
        ran = Math.floor(Math.random() * (data.articles.length - 2));
        featuredArticle = data.articles[ran];
        if (featuredArticle.img != null && featuredArticle.bio != null) {
            if (featuredArticle.classification != "NEWSLETTER" || ran == recentNewsletter) {
                break;
            }
        }
    }

    return featuredArticle;
}

